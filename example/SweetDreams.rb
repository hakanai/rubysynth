    bass_track.notes << Note.new("", 1, 16)
  }
  
  voice_track = Track.new(voice)
  4.times {
    voice_track.notes << Note.new("", 1, 1)
  }
  voice_track.notes << Note.new("", 1, 4)
  voice_track.notes << Note.new("C", 4, 8)
  voice_track.notes << Note.new("C", 4, 16)
  voice_track.notes << Note.new("", 4, 16)
  voice_track.notes << Note.new("C", 4, 8)
  voice_track.notes << Note.new("C", 4, 16)
  voice_track.notes << Note.new("", 4, 16)
  voice_track.notes << Note.new("A", 4, 4)
  voice_track.notes << Note.new("C", 4, 16)
  voice_track.notes << Note.new("C", 4, 32)
  voice_track.notes << Note.new("", 4, 32)
  voice_track.notes << Note.new("C", 4, 8)
  voice_track.notes << Note.new("C", 4, 16)
  voice_track.notes << Note.new("", 4, 16)
  voice_track.notes << Note.new("C", 4, 8)
  voice_track.notes << Note.new("B", 4, 2)
  
