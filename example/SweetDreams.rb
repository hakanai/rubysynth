def sweet_dreams
  bpm = 140
  noise_drum = Instrument.new(bpm, NoiseOscillator.new(44100, 123.0, 0.6), nil, nil)
  synth_bass = Instrument.new(bpm, OvertoneAdder.new(SawtoothOscillator.new(44100, 123.0, 0.3), [1.0]), nil, nil)
  voice = Instrument.new(bpm, OvertoneAdder.new(SineOscillator.new(44100, 220.0, 0.8), [0.1, 0.4]), nil, nil)
  
  drum_track = Track.new(noise_drum)
  24.times {
    drum_track.notes << Note.new("", 0, 4)
    drum_track.notes << Note.new("A", 0, 16)
    drum_track.notes << Note.new("", 0, 16)
    drum_track.notes << Note.new("", 0, 8)
  }
  
  bass_track = Track.new(synth_bass)
  6.times {
    bass_track.notes << Note.new("A", 0, 16)
    bass_track.notes << Note.new("", 1, 16)
    bass_track.notes << Note.new("A", 1, 16)
