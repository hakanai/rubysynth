  voice_track.notes << Note.new("A", 4, 32)
  voice_track.notes << Note.new("", 4, 32)
  voice_track.notes << Note.new("C", 4, 8)
  voice_track.notes << Note.new("D", 4, 4)
  voice_track.notes << Note.new("D", 4, 16)
  voice_track.notes << Note.new("", 4, 16)
  voice_track.notes << Note.new("C", 4, 8)
  voice_track.notes << Note.new("B", 4, 8)
  voice_track.notes << Note.new("", 4, 8)
  voice_track.notes << Note.new("A", 4, 8)
  
  voice_track.notes << Note.new("C", 4, 16)
