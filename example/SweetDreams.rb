  voice_track.notes << Note.new("", 4, 32)
  voice_track.notes << Note.new("C", 4, 8)
  voice_track.notes << Note.new("B", 4, 4)
  voice_track.notes << Note.new("", 4, 4)
  
  voice_track.notes << Note.new("C", 4, 4)
  voice_track.notes << Note.new("A", 4, 8)
  voice_track.notes << Note.new("C", 4, 4)
  voice_track.notes << Note.new("A", 4, 4)
  voice_track.notes << Note.new("A", 4, 8)
  voice_track.notes << Note.new("C", 4, 16)
  voice_track.notes << Note.new("C", 4, 32)
  voice_track.notes << Note.new("", 4, 32)
  voice_track.notes << Note.new("C", 4, 8)
  voice_track.notes << Note.new("D", 4, 8)
  voice_track.notes << Note.new("C", 4, 4)
  voice_track.notes << Note.new("B", 4, 4)

  
  s = Song.new()
  s.tracks = [drum_track, bass_track, voice_track]
  
  return s.next_samples(s.sample_length)
